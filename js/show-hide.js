function showI() {
  if (document.getElementById('table-1').style.display == 'none') {
    document.getElementById('yb-1').style.backgroundColor = '#c49815';
    document.getElementById('yb-2').style.backgroundColor = '';
    document.getElementById('yb-3').style.backgroundColor = '';
    document.getElementById('yb-4').style.backgroundColor = '';
    document.getElementById('yb-5').style.backgroundColor = '';

    document.getElementById('table-1').style.display = 'block';
    document.getElementById('table-2').style.display = 'none';
    document.getElementById('table-3').style.display = 'none';
    document.getElementById('table-4').style.display = 'none';
    document.getElementById('table-5').style.display = 'none';
    document.getElementById('home').style.display = 'none';
  } else {
    document.getElementById('yb-1').style.backgroundColor = '';
    document.getElementById('yb-2').style.backgroundColor = '';
    document.getElementById('yb-3').style.backgroundColor = '';
    document.getElementById('yb-4').style.backgroundColor = '';
    document.getElementById('yb-5').style.backgroundColor = '';

    document.getElementById('table-1').style.display = 'none';
    document.getElementById('table-2').style.display = 'none';
    document.getElementById('table-3').style.display = 'none';
    document.getElementById('table-4').style.display = 'none';
    document.getElementById('table-5').style.display = 'none';
    document.getElementById('home').style.display = 'block';
  }
}

